close all;
filter = fspecial('gaussian', 20, 10);
I1 = rec0x2D00009820;
I2 = rec0x2D00009821;
blur1 = imfilter(I1, filter);
blur2 = imfilter(I2, filter);
[u, v] = HS(blur1, blur2, 12);
s = calcSmoothness(u,v,10, rgb2gray(I1), rgb2gray(I2), 80);
figure;
imshow(s);
edges = findEdges(I1) | findEdges(I2);
%imshow(s>mean(mean(s))&edges);
figure;
imshow(s > 0.001 & edges);